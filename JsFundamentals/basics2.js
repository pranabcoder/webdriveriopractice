/*
Example - Conditional Statements
 */

const flag = true;
if (flag) {
    console.log("Flag is true");
} else if (!flag) {
    console.log("Flag is false");
} else {
    console.log("Flag is not a boolean");
}

if (!flag) {
    console.log("Condition Satisfied");
} else {
    console.log("Condition Not Satisfied");
}

